def read_toml_file( ftoml )

  unless File.file?( ftoml ) then return end

  subj = Tomlrb.load_file( ftoml )

  subj['COMP'].each do | comp |

    page = read_html_from_url_to_file( comp['url'] )

    page.xpath( '//a' ).each do | node |

      next unless node.first[1] =~ /htm$/

      m1     =   comp['url'].split( "/" )
      m2     = node.first[1].split( "/" )
      m1[-2] = m2[-2]
      m1[-1] = m2[-1]
      url    = m1.join( '/' )

      read_html_from_url_to_file( url )
    end
  end
end

